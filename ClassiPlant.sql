Create schema ClassiPlant;

USE ClassiPlant;

-- Create the 'Flower' table
CREATE TABLE Flower (
    flower VARCHAR(50) PRIMARY KEY,
    blooming_season VARCHAR(50),
    petal_num INT,
    shape VARCHAR(50),
    color VARCHAR(50),
    CONSTRAINT CHK_PetalNum CHECK (petal_num >= 0)
);

-- Create the 'Fruit' table
CREATE TABLE Fruit (
    fruit VARCHAR(50) PRIMARY KEY,
    season VARCHAR(50),
    color VARCHAR(50),
    shape VARCHAR(50),
    size DOUBLE
);

-- Create the 'Leaf' table
CREATE TABLE Leaf (
    leafID INT PRIMARY KEY,
    leaf_shape VARCHAR(50) NOT NULL,
    leaf_margins VARCHAR(50),
    foliage_type VARCHAR(50),
    surface_texture VARCHAR(50),
    length DOUBLE,
    CONSTRAINT CHK_Length CHECK (length > 0)
);

-- Create the 'Safety' table
CREATE TABLE Safety (
	safeID INT PRIMARY KEY,
    edibility VARCHAR(50),
    dangers VARCHAR(200)
);

-- Create the 'Plant_name' table
CREATE TABLE Plant_name (
	nameID INT PRIMARY KEY,
    common_name VARCHAR(50),
    scientific_name VARCHAR(50),
    CONSTRAINT UNQ_CommonName UNIQUE (common_name),
    CONSTRAINT UNQ_ScientificName UNIQUE (scientific_name)
);

CREATE TABLE Shrub (
	shrubID INT PRIMARY KEY,
    flower VARCHAR(50),
    fruit VARCHAR(50),
    min INT,
    max INT,
    CONSTRAINT CHK_ShrubHeight CHECK (min <= max),
    CONSTRAINT FK_Shrub_Plant_Name FOREIGN KEY (shrubID) REFERENCES Plant_Name (nameID) ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT FK_Shrub_Fruit FOREIGN KEY (fruit) REFERENCES Fruit (fruit) ON DELETE SET NULL ON UPDATE CASCADE,
    CONSTRAINT FK_Shrub_Flower FOREIGN KEY (flower) REFERENCES Flower (flower) ON DELETE SET NULL ON UPDATE CASCADE,
    CONSTRAINT FK_Shrub_Leaf FOREIGN KEY (shrubID) REFERENCES Leaf (leafID) ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT FK_Shrub_Safety FOREIGN KEY (shrubID) REFERENCES Safety (safeID) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE Herbs_Groundcover (
	herbID INT PRIMARY KEY,
    flower VARCHAR(50),
    fruit VARCHAR(50),
    min INT,
    max INT,
    CONSTRAINT CHK_Herbs_GroundcoverHeight CHECK (min <= max),
    CONSTRAINT FK_HG_Plant_Name FOREIGN KEY (herbID) REFERENCES Plant_Name (nameID) ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT FK_HG_Fruit FOREIGN KEY (fruit) REFERENCES Fruit (fruit) ON DELETE SET NULL ON UPDATE CASCADE,
    CONSTRAINT FK_HG_Flower FOREIGN KEY (flower) REFERENCES Flower (flower) ON DELETE SET NULL ON UPDATE CASCADE,
    CONSTRAINT FK_HG_Leaf FOREIGN KEY (herbID) REFERENCES Leaf (leafID) ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT FK_HG_Safety FOREIGN KEY (herbID) REFERENCES Safety (safeID) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE Tree (
    treeID INT PRIMARY KEY,
    flower VARCHAR(50),
    fruit VARCHAR(50),
    color VARCHAR(50),
    bark_type VARCHAR(50),
    min INT,
    max INT,
    CONSTRAINT CHK_TreeHeight CHECK (min <= max),
    CONSTRAINT FK_Tree_Plant_Name FOREIGN KEY (treeID) REFERENCES Plant_Name (nameID) ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT FK_Tree_Fruit FOREIGN KEY (fruit) REFERENCES Fruit (fruit) ON DELETE SET NULL ON UPDATE CASCADE,
    CONSTRAINT FK_Tree_Flower FOREIGN KEY (flower) REFERENCES Flower (flower) ON DELETE SET NULL ON UPDATE CASCADE,
    CONSTRAINT FK_Tree_Leaf FOREIGN KEY (treeID) REFERENCES Leaf (leafID) ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT FK_Tree_Safety FOREIGN KEY (treeID) REFERENCES Safety (safeID) ON DELETE CASCADE ON UPDATE CASCADE
);



